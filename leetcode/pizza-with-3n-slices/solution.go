const nmax = 502

func maxSizeSlices(slices []int) int {
	a := [nmax]uint16{}
	memo := map[[nmax]uint16]int{}

	for i, n := range slices {
		a[i] = uint16(n)
	}
	a[nmax-1] = uint16(len(slices))

	var solve func(a [nmax]uint16) int
	solve = func(a [nmax]uint16) int {
		if a[0] == 0 {
			return 0
		}

		if r, f := memo[a]; f {
			return r
		}

		max, l := 0, int(a[nmax-1])
		/*for i := 0; i < l; i++ {
		    fmt.Printf("%v ", a[i])
		  }
		  fmt.Println()*/

		if l == 3 {
			for i := 0; i < 3; i++ {
				if int(a[i]) > max {
					max = int(a[i])
				}
			}

			l = 0
		}

		for i := 0; i < l; i++ {
			next := [nmax]uint16{}
			k := 0
			for j := 0; j < l; j++ {
				if j == i || j == (i+1)%l || j == (i-1+l)%l {
					continue
				}
				next[k] = a[j]
				k++
			}

			next[nmax-1] = a[nmax-1] - 3

			s := int(a[i]) + solve(next)
			if s > max {
				max = s
			}
		}

		memo[a] = max

		return max
	}

	r := solve(a)

	return r
}

