/*
Answer: How to add negabinary numbers? https://math.stackexchange.com/a/3251611?stw=2

You add them just like regular binary numbers, except the carry is negative and should be subtracted from each column instead of added to it.
*/

/**
 * @param {number[]} arr1
 * @param {number[]} arr2
 * @return {number[]}
 */
var addNegabinary = function(arr1, arr2) {
  let [ i, j ] = [ arr1.length - 1, arr2.length - 1 ]

  let res = []
  let carry = 0
  for (; i >= 0 || j >= 0 || carry != 0; i--, j--) {
    let a = i >= 0 ? arr1[i] : 0
    let b = j >= 0 ? arr2[j] : 0

    let nextBit = a + b + carry
    carry = 0

    if (nextBit < 0) {
      nextBit = 1
      carry = 1
    } else {
      if (nextBit > 1) {
        carry = -1
        nextBit %= 2
      }
    }

    // console.log(a, b, carry)

    res.unshift(nextBit)
  }

  while (res.length > 1 && !res[0]) {
    res.shift()
  }

  return res
}

